class API {
  constructor() {
    this.ready = false;
  }

  async _load() {
    try {
      if (typeof this.startAPI === 'function') {
        await this.startAPI();
      }
      this.log('API loaded');
      this.ready = true;
    } catch (e) {
      this.error('An error occurred during initialization!', e);
    }
  }

  async _unload() {
    try {
      if (typeof this.apiWillUnload === 'function') {
        await this.apiWillUnload();
      }
      this.ready = false;
      this.log('Plugin unloaded');
    } catch (e) {
      this.error('An error occurred during shutting down! It\'s heavily recommended to reload Discord to ensure there is no conflicts.', e);
    }
  }

  log(...data) {
    console.log(`%c[Flowcord:API:${this.constructor.name}]`, `color: ${consoleColor}`, ...data);
  }

  error(...data) {
    console.error(`%c[Flowcord:API:${this.constructor.name}]`, `color: ${consoleColor}`, ...data);
  }
}

module.exports = API;
